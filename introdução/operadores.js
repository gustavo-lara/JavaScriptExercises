// Operadores de atribuição aritimética 
var x = 10;
var y = 5;
console.log("x =", x, " | y =", y);

x += y;    // x = x + y;
console.log("x =", x," | y=", y);

x -= y;    // x = x - y;
console.log("x =", x," | y=", y);

x *= y;    // x = x * y;
console.log("x =", x," | y=", y);

x /= y;    // x = x / y;
console.log("x =", x," | y=", y);

x **= y;    // x = x elevado à y;
console.log("x =", x," | y=", y);

x %= y;    // x = resto da divisão inteira de x por y;
console.log("x =", x," | y=", y);

x = 13;
x %= y;    // x = resto da divisão inteira de x por y;
console.log("x =", x," | y=", y);
