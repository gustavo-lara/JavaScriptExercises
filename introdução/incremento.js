// Operadores de incremento e decremento / pré o pós incremento
var idade = 16;
console.log(idade);

idade++;                  //idade = idade + 1
console.log(idade);

idade--;                  //idade = idade + 1
console.log(idade);

console.log("--------------------");

var novaIdade = idade++;
console.log("Idade:", idade," | Nova idade:", novaIdade);

var novaIdade = ++idade;
console.log("Idade:", idade," | Nova idade:", novaIdade);



